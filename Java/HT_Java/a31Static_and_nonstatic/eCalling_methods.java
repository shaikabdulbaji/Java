package a31Static_and_nonstatic;

public class eCalling_methods {
	
	/*
	 * static methods lo static vatini call chaiyavachu non-static vatini call chaiyaleamu ala 
	   call chaiyali antea object or instance create chaiyali.
	   new eCalling_methods() --> instance or object.
	 */
	
	public static void main(String[] args) {
		new eCalling_methods().print();
	}
	
	public void print() {
		main(null);
	}

}
