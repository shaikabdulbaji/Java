package a18Operators;

public class dRelational_Operators {

	public static void main(String[] args) {
		
		int i=20;
		int j=6;
		
		System.out.println(i==j);
		System.out.println(i!=j);
		System.out.println(i<j);
		System.out.println(i<=j);
		System.out.println(i>j);
		System.out.println(i>=j);
	}

}
